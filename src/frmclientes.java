    private void btningresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresarActionPerformed
          Connection conexion=conexionmysql.getConnection();
        try {
            Statement st=conexion.createStatement();
            int id= Integer.parseInt(txtid.getText());
            String nombre=txtnombre.getText();
            int cel= Integer.parseInt(txtcel.getText());
            String correo=txtcorreo.getText();
            String direccion=txtdireccion.getText();
            int x=st.executeUpdate("Insert into clientes values ("+id+",'"+nombre+"',"+cel+",'"+correo+"','"+direccion+"')");
            if (x==1)
            {
                JOptionPane.showMessageDialog(null,"registro exitoso");
            
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btningresarActionPerformed

    private void btnlistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnlistarActionPerformed
          Connection max=conexionmysql.getConnection();
        DefaultTableModel modelo=new DefaultTableModel();
        modelo.setColumnIdentifiers(new Object[]{"id","nombre","cel","correo","direccion"});
        try {
            Statement st=max.createStatement();
            ResultSet rs=st.executeQuery("Select * from clientes");
            while (rs.next())
            {
                modelo.addRow(new Object[]{rs.getInt("id"),rs.getString("nombre"),rs.getInt("cel"),rs.getString("correo"),rs.getString("direccion")});
            }
            datoclientes.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(frmempleados.class.getName()).log(Level.SEVERE, null, ex);
        
    }//GEN-LAST:event_btnlistarActionPerformed
    }
    
    
