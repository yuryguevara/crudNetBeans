    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
          Connection mi=conexionmysql.getConnection();
       try {
           Statement st=mi.createStatement();
            
            int x=st.executeUpdate("Update clientes set nombre='"+txtnombre.getText()+"',cel='"+txtcel.getText()+"',correo='"+txtcorreo.getText()+"',direccion='"+txtdireccion.getText()+"' where id='"+txtid.getText()+"'");
            if (x==1)
            {
                JOptionPane.showMessageDialog(null,"datos actualizados");
            
            }
          
       } catch (SQLException ex) {
           Logger.getLogger(frmempleados.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_btnactualizarActionPerformed

